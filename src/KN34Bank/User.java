package KN34Bank;

public class User {

    private String userName;
    private int userID;

    public void setUserName (String userName) {
        this.userName = userName;
    }

    public String getUserName () {
        return userName;
    }

    public void setUserId (int userID) {
        this.userID = userID;
    }

    public int getUserID () {
        return userID;
    }
}
